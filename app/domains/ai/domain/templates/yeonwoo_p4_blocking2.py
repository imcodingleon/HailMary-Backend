_HEAVEN_HANJA: dict[str, str] = {
    "갑": "甲", "을": "乙", "병": "丙", "정": "丁", "무": "戊",
    "기": "己", "경": "庚", "신": "辛", "임": "壬", "계": "癸",
}
_OHANG_HANJA: dict[str, str] = {
    "목": "木", "화": "火", "토": "土", "금": "金", "수": "水",
}


def _ilgan_with_hanja(ilgan: str) -> str:
    """갑목 → 갑목(甲木)."""
    if len(ilgan) != 2:
        return ilgan
    h = _HEAVEN_HANJA.get(ilgan[0])
    o = _OHANG_HANJA.get(ilgan[1])
    if not h or not o:
        return ilgan
    return f"{ilgan}({h}{o})"


def _ohang_with_hanja(ohang: str) -> str:
    """수 → 수(水)."""
    h = _OHANG_HANJA.get(ohang)
    if not h:
        return ohang
    return f"{ohang}({h})"


