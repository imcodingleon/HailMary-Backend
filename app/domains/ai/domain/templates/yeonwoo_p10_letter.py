def compose_box2_body(
    *,
    ilgan: str,
    step2: tuple[str, ...],
    keyword_tags: tuple[str, ...] | None = None,
    peak_month_labels: tuple[str, str] | None = None,
) -> str:
    """박스 2 본문 풀 합성 (4단락).

    Args:
        ilgan: 일간 한글 (10종)
        step2: 사용자 선택 step2 슬러그 튜플 (1~4개)
        keyword_tags: P-6 keyword_tags (5개) — soulmate 답에 박힘. 없으면 폴백 텍스트.
        peak_month_labels: P-8 peak 월 라벨 2개 ("6월", "'27. 3월") — timing 답에 박힘. 없으면 폴백.

    Returns:
        합성된 박스 2 본문 (4단락 \n\n join).
    """
    # PARA1 — step2 부분집합 인정
    subset_key = _normalize_step2_key(step2)
    para1 = PARA1_BY_STEP2_SUBSET[subset_key]

    # PARA2 — 일간 본질 + 부사 연결
    para2 = PARA2_BY_ILGAN[ilgan]

    # PARA3 — step2 항목별 답 누적 (STEP2_PRIORITY 순서 — 우선순위 높은 항목부터)
    priority_order = {s: i for i, s in enumerate(STEP2_PRIORITY)}
    sorted_keys = tuple(sorted(step2, key=lambda x: priority_order.get(x, 999)))
    para3_chunks: list[str] = []
    for s2 in sorted_keys:
        body = PARA3_BY_STEP2_ILGAN[(ilgan, s2)]
        # placeholder 치환
        if s2 == "soulmate" and keyword_tags:
            tags_text = " · ".join(keyword_tags[:3])  # 상위 3개 태그
            body = body.format(keyword_tags_text=tags_text)
        elif s2 == "soulmate":
            body = body.format(keyword_tags_text="너의 깊이를 알아보는 결의 사람")
        elif s2 == "timing" and peak_month_labels:
            body = body.format(
                peak_1_label=peak_month_labels[0],
                peak_2_label=peak_month_labels[1],
            )
        elif s2 == "timing":
            body = body.format(
                peak_1_label="피크 1",
                peak_2_label="피크 2",
            )
        para3_chunks.append(body)
    para3 = "\n\n".join(para3_chunks)

    # PARA4 — 일간 마무리
    para4 = PARA4_BY_ILGAN[ilgan]

    return "\n\n".join([para1, para2, para3, para4])


def _normalize_step2_key(slugs: tuple[str, ...]) -> str:
    """step2 슬러그 튜플 → 정렬된 키. 예) ("timing", "patterns") → "patterns+timing"."""
    return "+".join(sorted(slugs))


