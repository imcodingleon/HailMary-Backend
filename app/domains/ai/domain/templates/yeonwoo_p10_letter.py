# 박스 3 합성 함수
# ═════════════════════════════════════════════════════════════════

def compose_box3(
    *,
    ilgan: str,
    step3: str | None,
    step1: tuple[str, ...] = (),
    ai_letter_body: str | None = None,
) -> dict[str, object]:
    """박스 3 풀 합성.

    Args:
        ilgan: 일간 한글 (10종)
        step3: 사용자 자유 텍스트. None/빈 문자열이면 폴백.
        step1: step1 슬러그 튜플. step3 비었을 때 폴백 본문 1순위 픽에 사용.
        ai_letter_body: AI 호출 결과 (step3 있을 때만 사용). None이면 폴백.

    Returns:
        {
          quote_text: str,                # step3 raw 또는 "..."
          quote_label: str,               # — 네가 적은 너의 고민 / 적지 못한 너의 고민
          body: str,                      # AI 결과 or 폴백 합성
          emphasis: str,                  # 강조구 (일간 10셀)
          tail: str,                      # 고정 꼬리
          uses_ai: bool,                  # AI 호출 여부
        }
    """
    has_step3 = step3 is not None and step3.strip() != ""

    # ① Quote 영역
    if has_step3:
        quote_text = step3.strip()
        quote_label = QUOTE_LABEL_WITH_TEXT
    else:
        quote_text = QUOTE_TEXT_EMPTY
        quote_label = QUOTE_LABEL_EMPTY

    # ② 답장 단락
    if has_step3 and ai_letter_body:
        body = ai_letter_body
        uses_ai = True
    else:
        # 폴백 합성 (도입 + 본문 + 마무리)
        primary_step1 = _priority_pick_step1(step1)
        fallback_body = BOX3_FALLBACK_BODY_BY_STEP1_ILGAN.get(
            (ilgan, primary_step1),
            BOX3_FALLBACK_BODY_BY_STEP1_ILGAN[(ilgan, "waiting_new")],  # 안전 폴백
        )
        body = f"{BOX3_FALLBACK_INTRO}\n\n{fallback_body}\n\n{BOX3_FALLBACK_OUTRO}"
        uses_ai = False

    # ③ 강조구
    emphasis = EMPHASIS_BY_ILGAN[ilgan]

    # ④ 꼬리 (고정)
    tail = BOX3_TAIL

    return {
        "quote_text": quote_text,
        "quote_label": quote_label,
        "body": body,
        "emphasis": emphasis,
        "tail": tail,
        "uses_ai": uses_ai,
    }


def _priority_pick_step1(step1: tuple[str, ...]) -> str:
    """step1 슬러그 튜플에서 우선순위 1위 픽. 빈 튜플이면 'waiting_new' 기본값."""
    if not step1:
        return "waiting_new"
    for s in STEP1_PRIORITY:
        if s in step1:
            return s
    return step1[0]


# ═════════════════════════════════════════════════════════════════
# 박스 1+2+3 통합 합성 함수
# ═════════════════════════════════════════════════════════════════

def compose_box1_body(
    *,
    ilgan: str,
    step1: tuple[str, ...],
) -> str:
    """박스 1 본문 합성 (도입 멘트 + 본문 셀).

    Args:
        ilgan: 일간 한글
        step1: step1 슬러그 튜플 (1~4개)

    Returns:
        BOX1_INTRO_FIXED + "\\n\\n" + step1 부분집합 본문 셀.
    """
    subset_key = "+".join(sorted(step1))
    body = BOX1_BODY_BY_ILGAN_STEP1[(ilgan, subset_key)]
    return f"{BOX1_INTRO_FIXED}\n\n{body}"


