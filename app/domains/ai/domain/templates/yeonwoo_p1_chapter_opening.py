# ── 한자 병기 헬퍼 ─────────────────────────────────
# dict 값은 한글로 두고, 합성 시점에 "갑목" → "갑목(甲木)" 자동 변환.

_HEAVEN_HANJA: dict[str, str] = {
    "갑": "甲", "을": "乙", "병": "丙", "정": "丁", "무": "戊",
    "기": "己", "경": "庚", "신": "辛", "임": "壬", "계": "癸",
}

_EARTH_HANJA: dict[str, str] = {
    "자": "子", "축": "丑", "인": "寅", "묘": "卯", "진": "辰", "사": "巳",
    "오": "午", "미": "未", "신": "申", "유": "酉", "술": "戌", "해": "亥",
}

_OHANG_HANJA: dict[str, str] = {
    "목": "木", "화": "火", "토": "土", "금": "金", "수": "水",
}


def _ilgan_with_hanja(ilgan: str) -> str:
    """갑목 → 갑목(甲木)"""
    if len(ilgan) != 2:
        return ilgan
    heaven, ohang = ilgan[0], ilgan[1]
    h_hanja = _HEAVEN_HANJA.get(heaven)
    o_hanja = _OHANG_HANJA.get(ohang)
    if not h_hanja or not o_hanja:
        return ilgan
    return f"{ilgan}({h_hanja}{o_hanja})"


def _ilju_with_hanja(ilju: str) -> str:
    """갑자 → 갑자(甲子)"""
    if len(ilju) != 2:
        return ilju
    heaven, earth = ilju[0], ilju[1]
    h_hanja = _HEAVEN_HANJA.get(heaven)
    e_hanja = _EARTH_HANJA.get(earth)
    if not h_hanja or not e_hanja:
        return ilju
    return f"{ilju}({h_hanja}{e_hanja})"


# 천간 한글 → 오행 한글 (명리학 룰)
_HEAVEN_TO_OHANG: dict[str, str] = {
    "갑": "목", "을": "목",
    "병": "화", "정": "화",
    "무": "토", "기": "토",
    "경": "금", "신": "금",
    "임": "수", "계": "수",
}


def ilju_to_ilgan(ilju: str) -> str:
    """일주 → 일간 자동 추출 (명리학 룰: 일주 첫 글자가 곧 일간 천간).

    예: 갑자 → 갑목, 신축 → 신금, 임술 → 임수.

    Raises:
        KeyError: 일주의 첫 글자가 알려진 천간이 아닐 때.
    """
    if len(ilju) < 1:
        raise KeyError(f"invalid ilju: {ilju!r}")
    heaven = ilju[0]
    ohang = _HEAVEN_TO_OHANG.get(heaven)
    if ohang is None:
        raise KeyError(f"unknown heaven stem in ilju: {ilju!r}")
    return f"{heaven}{ohang}"


