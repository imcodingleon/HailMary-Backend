def compose_p1_chapter_opening(
    *,
    ilgan: str,
    ilju: str,
) -> str:
    """4단락 합성 — 일간/일주 독립 변수, 60 조합 출력 가능.

    명리학 룰: 일주의 첫 글자가 곧 일간 천간. 함수 호출 시 일관성 검증.

    Args:
        ilgan: 일간 한글 (10종)
        ilju: 일주 한글 (60갑자)

    Returns:
        4단락 합성 텍스트.

    Raises:
        KeyError: 알 수 없는 일간 또는 일주.
        ValueError: ilgan과 ilju의 천간이 불일치 (예: ilgan='갑목' + ilju='을묘').
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    if ilju not in VALID_ILJU:
        raise KeyError(f"unknown ilju: {ilju!r}")
    expected_ilgan = ilju_to_ilgan(ilju)
    if ilgan != expected_ilgan:
        raise ValueError(
            f"ilgan/ilju 불일치: ilgan={ilgan!r}이지만 ilju={ilju!r}의 일간은 "
            f"{expected_ilgan!r}이어야 함. 명리학 룰: 일주 첫 글자 = 일간 천간."
        )

    # 2단락: 일간 첫 등장 한자 병기.
    para2 = PARA_2_ILGAN[ilgan].replace(
        f"{ilgan} 일간",
        f"{_ilgan_with_hanja(ilgan)} 일간",
        1,
    )
    # 3단락: HTML 원본 패턴 "{ILJU} 일주는 거기에 한 겹 더 얹은 결이야. ..." 자동 prefix.
    # PARA_3_ILJU dict는 prefix 이후의 비유+결론만 담고 있어 합성 시점에 prefix 박음.
    prefix = PARA_3_ILJU_PREFIX_TEMPLATE.format(
        ilju_with_hanja=_ilju_with_hanja(ilju)
    )
    para3 = f"{prefix} {PARA_3_ILJU[ilju]}"

    return "\n\n".join([
        PARA_1_INTRO[ilgan],
        para2,
        para3,
        PARA_4_LOVETYPE[ilgan],
    ])
