# ── 강연우 버블 3개 (HTML 원본 고정) ──
BUBBLE_BLOCKADE: str = "내 눈엔 보여. 네 명줄에 뭔가 걸려 있어."
BUBBLE_PATTERN: str = "사람은 바뀌었는데 매듭 모양이 똑같아."
BUBBLE_REVERSE: str = "이게 독인지 약인지는 네가 어떻게 쓰냐에 달려있어."


VALID_ILGAN: frozenset[str] = frozenset(ILGAN_TRAIT_FOR_BLOCKADE.keys())
VALID_OHANG: frozenset[str] = frozenset(BLOCKADE_BY_OHANG.keys())


def compose_p3_blocking(*, ilgan: str, ohang_excess: str) -> dict:
    """P-3 풀 합성 (2-1 + 2-2 + 2-2-1).

    Args:
        ilgan: 일간 한글 (10종)
        ohang_excess: 과다 오행 한글 (5종: 목/화/토/금/수)

    Returns:
        {
          ohang_hanja, blockade_card_sub, ai_blockade,
          pattern_body, ai_pattern,
          reverse_card_1: {value, sub},
          reverse_card_2: {value, sub},
          bubble_blockade, bubble_pattern, bubble_reverse,
        }

    Raises:
        KeyError: 알 수 없는 일간/오행.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    if ohang_excess not in VALID_OHANG:
        raise KeyError(f"unknown ohang_excess: {ohang_excess!r}")

    base = BLOCKADE_BY_OHANG[ohang_excess]
    trait = ILGAN_TRAIT_FOR_BLOCKADE[ilgan]

    ai_blockade = "\n\n".join([
        base["para_1_intro"],
        base["para_2_blockade"],
        trait,  # ← 두 번째 문단 아래 일간 한 줄
        base["para_4_remedy"],
    ])

    pattern = PATTERN_BY_ILGAN[ilgan]
    reverse = REVERSE_BY_ILGAN[ilgan]

    return {
        # 2-1
        "ohang_excess": OHANG_HANJA[ohang_excess],  # 한자 포함 표기값 "수(水)"
        "blockade_card_sub": base["sub"],
        "ai_blockade": ai_blockade,
        # 2-2
        "pattern_body": pattern["body"],
        "ai_pattern": pattern["ai"],
        # 2-2-1
        "reverse_card_1": reverse["card_1"],
        "reverse_card_2": reverse["card_2"],
        # 강연우 버블 (고정)
        "bubble_blockade": BUBBLE_BLOCKADE,
        "bubble_pattern": BUBBLE_PATTERN,
        "bubble_reverse": BUBBLE_REVERSE,
    }
