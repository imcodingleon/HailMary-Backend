VALID_ILGAN: frozenset[str] = frozenset(ILGAN_PARA_2.keys())
VALID_OHANG: frozenset[str] = frozenset(OHANG_EXCESS_PARA_3.keys())


def compose_p0_intro(
    ilgan: str,
    ohang_excess: str,
    ohang_lack: str,
) -> str:
    """5문단 템플릿 합성. 220~280자 범위 출력.

    Args:
        ilgan: 일간 한글 (10종: 갑목/을목/병화/정화/무토/기토/경금/신금/임수/계수)
        ohang_excess: 과다 오행 (5종: 목/화/토/금/수)
        ohang_lack: 부족 오행 (5종)

    Returns:
        5문단 합성 텍스트.

    Raises:
        KeyError: 알 수 없는 일간 또는 오행.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    if ohang_excess not in VALID_OHANG:
        raise KeyError(f"unknown ohang_excess: {ohang_excess!r}")
    if ohang_lack not in VALID_OHANG:
        raise KeyError(f"unknown ohang_lack: {ohang_lack!r}")

    return "\n\n".join([
        PARA_1_OPENING,
        ILGAN_PARA_2[ilgan],
        OHANG_EXCESS_PARA_3[ohang_excess],
        OHANG_LACK_PARA_4[ohang_lack],
        PARA_5_CLOSING,
    ])
