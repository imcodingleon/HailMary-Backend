# ── 강연우 SD+버블 멘트 (HTML 원본 고정, 일간 공통) ──
RECOVERY_BUBBLE: str = "끊어진 실은 끊어진 거야. 계속 붙잡으려 하지 마."


VALID_ILGAN: frozenset[str] = frozenset(SPEED_BY_ILGAN.keys())


def get_recovery_speed(ilgan: str) -> RecoverySpeed:
    """일간별 회복 속도 반환."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    return SPEED_BY_ILGAN[ilgan]


def get_time_labels(ilgan: str) -> tuple[str, str, str]:
    """일간 회복 속도에 따른 3 카드 시간 라벨 반환."""
    speed = get_recovery_speed(ilgan)
    return TIME_LABELS_BY_SPEED[speed]


def compose_p2_recovery(*, ilgan: str) -> dict:
    """1-5 이별 후 회복 풀 합성.

    Args:
        ilgan: 일간 한글 (10종)

    Returns:
        {
          speed: "fast"|"medium"|"slow",
          timeline: [
            {time, title, desc},   # 직후
            {time, title, desc},   # 중간
            {time, title, desc},   # 마지막
          ],
          accel: {value, sub},
          ai_recovery: str,
          bubble: str (고정)
        }

    Raises:
        KeyError: 알 수 없는 일간.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")

    speed = SPEED_BY_ILGAN[ilgan]
    times = TIME_LABELS_BY_SPEED[speed]
    cards = RECOVERY_TIMELINE_BY_ILGAN[ilgan]
    accel_value, accel_sub = RECOVERY_ACCEL_BY_ILGAN[ilgan]

    return {
        "speed": speed,
        "timeline": [
            {"time": times[0], "title": cards[0][0], "desc": cards[0][1]},
            {"time": times[1], "title": cards[1][0], "desc": cards[1][1]},
            {"time": times[2], "title": cards[2][0], "desc": cards[2][1]},
        ],
        "accel": {"value": accel_value, "sub": accel_sub},
        "ai_recovery": RECOVERY_AI_BY_ILGAN[ilgan],
        "bubble": RECOVERY_BUBBLE,
    }
