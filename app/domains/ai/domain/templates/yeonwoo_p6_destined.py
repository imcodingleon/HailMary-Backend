_STEM_HANJA: dict[str, str] = {
    "갑": "甲", "을": "乙", "병": "丙", "정": "丁", "무": "戊",
    "기": "己", "경": "庚", "신": "辛", "임": "壬", "계": "癸",
}
_OHANG_HANJA: dict[str, str] = {
    "목": "木", "화": "火", "토": "土", "금": "金", "수": "水",
}


def _ilgan_with_hanja(ilgan: str) -> str:
    """갑목 → 갑목(甲木)."""
    if len(ilgan) != 2:
        return ilgan
    h = _STEM_HANJA.get(ilgan[0])
    o = _OHANG_HANJA.get(ilgan[1])
    if not h or not o:
        return ilgan
    return f"{ilgan}({h}{o})"


def _ohang_with_hanja(ohang: str) -> str:
    """수 → 수(水)."""
    o = _OHANG_HANJA.get(ohang)
    if not o:
        return ohang
    return f"{ohang}({o})"


VALID_ILGAN: frozenset[str] = frozenset(ILGAN_STABLE_FIT_BY_ILGAN.keys())
VALID_SLOTID: frozenset[str] = frozenset(SLOT_LOOKS_BY_SLOTID.keys())
VALID_OHANG: frozenset[str] = frozenset(OHANG_FIT_BY_LACK.keys())
VALID_ELEMENT_YINYANG: frozenset[str] = frozenset(KEYWORD_TAGS_BY_ELEMENT_YINYANG.keys())


def _slot_to_element_yinyang(slot_id: str) -> str:
    """'m-fire-yin' → 'fire-yin'. 매칭 상대 톤(element+yinyang) lookup용."""
    parts = slot_id.split("-", 1)
    return parts[1] if len(parts) == 2 else slot_id


def compose_p6_destined(
    *,
    ilgan: str,
    match_slot_id: str,
    ohang_lack: str,
) -> dict[str, object]:
    """P-6 4-1 붉은 실이 이어진 사람 풀 합성.

    Args:
        ilgan: 일간 한글 (10종)
        match_slot_id: 인연 슬롯 (20종, e.g. "m-water-yang"). neutral은 caller에서 차단.
        ohang_lack: 부족한 오행 한글 1글자 (5종)

    Returns:
        {
          match_slot_id, keyword_tags: list[str] (5), info_rows: list[{key, val}] (8),
          ai_looks: str (외형, 3~4단락),
          ai_match: str (오행+일간 매칭, 2단락),
          ai_first_meeting: str (첫 만남, 3단락),
          bubble: str (고정),
        }

    Raises:
        KeyError: 알 수 없는 일간/슬롯/오행.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    if match_slot_id not in VALID_SLOTID:
        raise KeyError(f"unknown match_slot_id: {match_slot_id!r}")
    if ohang_lack not in VALID_OHANG:
        raise KeyError(f"unknown ohang_lack: {ohang_lack!r}")

    ilgan_h = _ilgan_with_hanja(ilgan)
    ohang_h = _ohang_with_hanja(ohang_lack)
    ey = _slot_to_element_yinyang(match_slot_id)  # 매칭 상대 톤 키 ('fire-yin' 등)

    ai_looks = "\n\n".join([
        PARA_1_INTRO_LOOKS,
        SLOT_LOOKS_BY_SLOTID[match_slot_id],
    ])
    ai_match = "\n\n".join([
        OHANG_FIT_BY_LACK[ohang_lack].format(ohang_with_hanja=ohang_h),
        ILGAN_STABLE_FIT_BY_ILGAN[ilgan].format(ilgan_with_hanja=ilgan_h),
    ])

    inner_cards = [
        THREAD_STATE_BY_ILGAN[ilgan],
        BEHAVIOR_1_BY_ILGAN[ilgan],
        BEHAVIOR_2_BY_ILGAN[ilgan],
    ]

    return {
        # 4-1 (매칭 상대 결 — element_yinyang 기반)
        "match_slot_id": match_slot_id,
        "keyword_tags": list(KEYWORD_TAGS_BY_ELEMENT_YINYANG[ey]),
        "info_rows": [{"key": r.key, "val": r.val} for r in INFO_ROWS_BY_ELEMENT_YINYANG[ey]],
        "ai_looks": ai_looks,
        "ai_match": ai_match,
        "ai_first_meeting": FIRST_MEETING_BY_ILGAN[ilgan],
        "bubble": BUBBLE,
        # 4-2
        "inner_cards": [{"label": c.label, "value": c.value, "sub": c.sub} for c in inner_cards],
        "ai_inner": AI_INNER_BY_ILGAN[ilgan].format(ilgan_with_hanja=ilgan_h),
    }
