VALID_ILGAN: frozenset[str] = frozenset(ILGAN_MARU.keys())
VALID_CHARM_KEYS: frozenset[str] = frozenset(SAL_HANJA.keys())


def compose_charm_ai(*, sal_keys: tuple[str, ...], ilgan: str) -> str:
    """3-1 AI 박스 합성 — 보유 매력살 조합 + 일간 마무리.

    Args:
        sal_keys: 보유 매력살 키들 (정렬 무관, 0~6개)
        ilgan: 일간 한글 (10종)

    Returns:
        합성된 AI 박스 텍스트.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")

    sorted_keys = tuple(sorted(sal_keys))
    maru = ILGAN_MARU[ilgan]
    n = len(sorted_keys)

    if n == 0:
        main = NONE_PARAGRAPH
    elif n == 1:
        main = SINGLE_BY_SAL.get(sorted_keys[0], _multi_fallback(sorted_keys))
    elif n == 2:
        key = "+".join(sorted_keys)
        main = PAIR_BY_SALS.get(key, _multi_fallback(sorted_keys))
    elif n == 3:
        key = "+".join(sorted_keys)
        main = TRIPLE_BY_SALS.get(key, _multi_fallback(sorted_keys))
    elif n == 4:
        key = "+".join(sorted_keys)
        main = QUAD_BY_SALS.get(key, _multi_fallback(sorted_keys))
    elif n == 5:
        key = "+".join(sorted_keys)
        main = FIVE_BY_SALS.get(key, _multi_fallback(sorted_keys))
    elif n == 6:
        main = FULL_PARAGRAPH
    else:
        main = _multi_fallback(sorted_keys)

    return f"{main}\n\n{maru}"


def get_stage_cards(ilgan: str) -> list[dict[str, str]]:
    """3-2 4 단계 카드 합성 — 라벨 고정, value/sub 일간별."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    values = STAGE_VALUES_BY_ILGAN[ilgan]
    return [
        {"label": STAGE_LABELS[i], "value": values[i]["value"], "sub": values[i]["sub"]}
        for i in range(4)
    ]


def get_point_cards(ilgan: str) -> list[dict]:
    """3-3 3 카드 합성 — 라벨 고정, strength/flame/sub 일간별."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    data = POINT_DATA_BY_ILGAN[ilgan]
    return [
        {
            "label": POINT_LABELS[i],
            "strength": data[i]["strength"],
            "flame_label": data[i]["flame_label"],
            "sub": data[i]["sub"],
        }
        for i in range(3)
    ]


def get_mechanism_ai(ilgan: str) -> str:
    """3-2 AI 박스 (200~250자)."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    return MECHANISM_AI_BY_ILGAN[ilgan]


def get_sense_ai(ilgan: str) -> str:
    """3-3 AI 박스 (250~300자)."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    return SENSE_AI_BY_ILGAN[ilgan]


def compose_p5_charm(
    *,
    ilgan: str,
    charm_score: int,
    charm_percentile: int,
    user_sal_keys: tuple[str, ...],
) -> dict:
    """P-5 풀 합성 (3-1 + 3-2 + 3-3).

    Args:
        ilgan: 일간 한글 (10종)
        charm_score: charm_service.py charmStrength (0~100)
        charm_percentile: 상위 N% (예: 18 = 상위 18%, lookup_percentile 변환값)
        user_sal_keys: 보유 매력살 키 튜플 (0~6개)

    Returns:
        CharmPage 인터페이스 1:1 dict.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")

    # 매력살 카드 메타데이터 조회 (value_object/charm_sals.py에서)
    from ..value_object.charm_sals import CHARM_SALS

    charm_sals = []
    for k in user_sal_keys:
        if k in CHARM_SALS:
            m = CHARM_SALS[k]
            charm_sals.append({
                "charm_key": m.key,
                "name_kor": m.name_kor,
                "name_han": m.name_han,
                "trait": m.trait,
            })

    return {
        # 3-1
        "charm_score": charm_score,
        "charm_percentile": charm_percentile,
        "charm_sals": charm_sals,
        "ai_charm": compose_charm_ai(sal_keys=user_sal_keys, ilgan=ilgan),
        # 3-2
        "stage_cards": get_stage_cards(ilgan),
        "ai_mechanism": get_mechanism_ai(ilgan),
        # 3-3
        "point_cards": get_point_cards(ilgan),
        "ai_sense": get_sense_ai(ilgan),
    }
