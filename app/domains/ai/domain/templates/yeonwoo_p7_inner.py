_STEM_HANJA: dict[str, str] = {
    "갑": "甲", "을": "乙", "병": "丙", "정": "丁", "무": "戊",
    "기": "己", "경": "庚", "신": "辛", "임": "壬", "계": "癸",
}
_OHANG_HANJA: dict[str, str] = {
    "목": "木", "화": "火", "토": "土", "금": "金", "수": "水",
}


def _ilgan_with_hanja(ilgan: str) -> str:
    if len(ilgan) != 2:
        return ilgan
    h = _STEM_HANJA.get(ilgan[0])
    o = _OHANG_HANJA.get(ilgan[1])
    if not h or not o:
        return ilgan
    return f"{ilgan}({h}{o})"


VALID_ILGAN: frozenset[str] = frozenset(ENDING_NOW_BY_ILGAN.keys())


def compose_p7_inner(*, ilgan: str) -> dict[str, object]:
    """P-7 4-3 결말 예측 풀 합성.

    Args:
        ilgan: 일간 한글 (10종)

    Returns:
        {
          ending_card_1: {label, value, sub, tone},   # warn
          ending_card_2: {label, value, sub, tone},   # good
          ending_card_3: {label, value, sub, tone},   # amber
          ai_ending: str (4단락),
          notice: str (고정),
          bubble: str (고정),
        }

    Raises:
        KeyError: 알 수 없는 일간.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")

    ilgan_h = _ilgan_with_hanja(ilgan)

    def card(c: EndingCard) -> dict[str, str]:
        return {"label": c.label, "value": c.value, "sub": c.sub, "tone": c.tone}

    return {
        "ending_card_1": card(ENDING_NOW_BY_ILGAN[ilgan]),
        "ending_card_2": card(ENDING_YOU_BY_ILGAN[ilgan]),
        "ending_card_3": card(ENDING_OTHER_BY_ILGAN[ilgan]),
        "ai_ending": AI_ENDING_BY_ILGAN[ilgan].format(ilgan_with_hanja=ilgan_h),
        "notice": NOTICE_ENDING,
        "bubble": BUBBLE,
    }
