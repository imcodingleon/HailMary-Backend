def get_triggers(ilgan: str) -> tuple[str, str, str]:
    """일간별 사랑에 빠지는 트리거 3개 반환."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    return TRIGGERS_BY_ILGAN[ilgan]


def get_trigger_descriptions(ilgan: str) -> tuple[str, str, str]:
    """일간별 트리거 카드 csub 해석 3개 반환 (트리거 카드 안 한 줄 묘사)."""
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")
    return TRIGGER_DESCRIPTIONS_BY_ILGAN[ilgan]


def compose_p1_trigger(*, ilgan: str) -> str:
    """1-2 트리거 AI 박스 합성 — 200~250자.

    구조: 1단락(고정 도입 + 변동 트리거 + 고정 훅) + 2단락(일간별 클로징).

    Args:
        ilgan: 일간 한글 (10종)

    Returns:
        2단락 합성 텍스트.

    Raises:
        KeyError: 알 수 없는 일간.
    """
    if ilgan not in VALID_ILGAN:
        raise KeyError(f"unknown ilgan: {ilgan!r}")

    t1, t2, t3 = TRIGGERS_BY_ILGAN[ilgan]
    para1 = (
        f"{PARA_1_OPENING} {t1}, {t2}, 그리고 {t3}. {PARA_3_HOOK}"
    )
    intro = PARA_4_INTRO_BY_ILGAN[ilgan].format(
        ilgan_with_hanja=_ilgan_with_hanja(ilgan)
    )
    para4 = f"{intro} {PARA_4_HOOK}"

    return "\n\n".join([para1, para4])
